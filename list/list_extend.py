# Extend:- extend is use for add element into a list from another list

# Syntax:-  name of list.extand(name of second list)

#Example 1 :- 
lst = [1,'hello',2,'hi']
print('Before extend = ',lst)
l = [3,'harsh',4,'how',5,'are',6,'you']
lst.extend(l)
print('After extend = ',lst)

#  In extend we can add a string it is given below

